/**
 * 
 * Class to show examples of Logical Complement operator
 * 
 * @author G74S
 *
 */
public class _08LogicalComplement {
	public static void main(String [] args) {
		boolean flag = true;
		System.out.println(!flag);
		System.out.println(!!flag);
		
		
		/*
		 Output
		 	false
			true
		 
		 */
		
	}
	

}
