		System.out.print("->");
		System.out.print('\n');
		System.out.println("<-");
		
		System.out.print("->");
		System.out.print('\t');
		System.out.println("<-");
		
		System.out.print("->");
		System.out.print('\\');
		System.out.println("<-");
		
		System.out.print("->");
		System.out.print('\'');
		System.out.println("<-");
		
		System.out.println('\u0041'); // to get the unicode go to https://unicode-table.com/en, click the desired character to get the unicode value    u0041 -A
